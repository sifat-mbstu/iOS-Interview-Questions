// MARK: - SOLID Principles
//
// SOLID is a set of five object-oriented design principles.
// These principles help us write code that is easier to understand,
// easier to maintain, easier to test, and easier to extend.
//
// SOLID stands for:
//
// S - Single Responsibility Principle
// O - Open/Closed Principle
// L - Liskov Substitution Principle
// I - Interface Segregation Principle
// D - Dependency Inversion Principle

// MARK: - At a Glance: 10–15 Seconds Interview Preparation
//
// SOLID helps us design software in a clean and flexible way.
//
// S - Single Responsibility:
//     A class should have one clear responsibility.
//
// O - Open/Closed:
//     A class should be open for extension but closed for modification.
//
// L - Liskov Substitution:
//     A subclass should be usable wherever its parent class is expected.
//
// I - Interface Segregation:
//     A class should not be forced to depend on methods it does not use.
//
// D - Dependency Inversion:
//     High-level code should depend on abstractions, not concrete implementations.



